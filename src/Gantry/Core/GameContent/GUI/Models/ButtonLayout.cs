namespace Gantry.Core.GameContent.GUI.Models;

/// <summary>
/// Determines which buttons to display on the <see cref="MessageBox" /> GUI window.
/// </summary>
public enum ButtonLayout
{
    /// <summary>
    ///     Display only an OK button.
    /// </summary>
    Ok,

    /// <summary>
    ///     Display both an OK button, and a Cancel button.
    /// </summary>
    OkCancel
}